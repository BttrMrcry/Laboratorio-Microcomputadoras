include <p16f877.inc>
cteA equ 0x20
	ORG 0
GOTO INICIO
	ORG 5
INICIO:
	CLRF PORTA
	BSF STATUS, RP0
	BCF STATUS, RP1
	CLRF ADCON1
	CLRF PORTD
	BCF STATUS, RP0
	MOVLW B'11101001'
	MOVWF ADCON0
LOOP2:	
	BSF ADCON0, 2
	CALL RETARDO
LOOP:
	BTFSC ADCON0, 2
	GOTO LOOP 
	MOVF ADRESH, W
	MOVWF PORTD
	GOTO LOOP2


RETARDO:
	MOVLW 0X30
	MOVWF cteA
LOOPR:
	DECFSZ cteA
	GOTO LOOPR
	RETURN

END