PROCESSOR 16f877
INCLUDE <p16f877.inc> 
I equ H'00'; 
J equ H'00';
N equ H'16';
	ORG 0
 	GOTO INICIO 
 	ORG 5
INICIO: 
	BCF STATUS,RP1
	BCF STATUS,RP0; BANCO 0
	MOVLW 0X21
	MOVWF FSR

LOOP0:
	
LOOP1:
	MOVLW H'30'
	SUBWF FSR, W
	BTFSS STATUS, Z
	GOTO END_LOOP1
	INCF FSR
	GOTO LOOP1
END_LOOP1:
	GOTO LOOP0

	
	END